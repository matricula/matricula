     private void BuscarAlumnoEditar(String id)
     {
       
        
         String nom="", nom1="", ape="", ape1="", fech="", gen="" ;
         // Conectar con la base de datos 
         ConexionMySQL mysql= new ConexionMySQL();
         java.sql.Connection cn= mysql.Conectar();
         // La consulta de selección de datos
         String sSQL="SELECT idalumno, nombre1, nombre2, apellido1, apellido2, fecha, genero FROM alumnos" +
                       "WHERE idalumno="+id; 
          // Ejecute la consulta
        try {
            Statement st = cn.createStatement();
            ResultSet rs = st.executeQuery(sSQL);
            // Llene la tabla con los datos de registro y pasar estos datos a un elemento jTable1 del formulario 
            while (rs.next())
            {
                
                nom=rs.getString("nombre1");
                nom1=rs.getString("nombre2");
                ape=rs.getString("apellido1");
                ape1=rs.getString("apellido2");
                fech=rs.getString("fecha");
                gen=rs.getString("genero");
                
            }
                jTextField_Nombre1.setText(nom);
                jTextField_Nombre2.setText(nom1);
                jTextField_Apellido1.setText(ape);
                jTextField_Apellido2.setText(ape1);
                jTextField_Fecha.setText(fech);
                jComboBox1.setSelectedItem(gen);
            // En caso de error puede llenar el mensaje de error
            } catch (SQLException ex) {
              JOptionPane.showMessageDialog(null, "Mensaje de Error de lectura de datos"); 
            }
    }
