    private void jButtonDatosAlumnoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonDatosAlumnoActionPerformed
        // TODO add your handling code here:
         String Busca= jTextFieldModificar.getText();
          // Validar los datos que no sean blancos
        if ("".equals(Busca))
            {
            
                JOptionPane.showMessageDialog(null, "Debe ingresar Rut");
                return;
            }
        
        else
        {
            if (v1.solonumeros(Busca)== false)
            {
            JOptionPane.showMessageDialog(null, "Debe ingresar solo numeros en el campo Rut");
            return;
            }
        // un registro para recuperar los datos desde la consulta SQL
         String[] registro = new String[7];
         String[] Titulos={"Idalumno" , "Nombre1", "Nombre2", "Apellido1","Apellido2", "Fecha", "Genero"};
        // Un modelo contenedor para los datos que sean leidos  
        modelo = new DefaultTableModel(null,Titulos);
        // Conectar con la base de datos 
        ConexionMySQL mysql= new ConexionMySQL();
        java.sql.Connection cn= mysql.Conectar();
        // La consulta de selección de datos
        String sSQL="SELECT idalumno, nombre1, nombre2, apellido1, apellido2, fecha, genero FROM alumnos WHERE idalumno LIKE '%"+Busca+"%'";
        // Ejecute la consulta
        try {
            java.sql.Statement st = cn.createStatement();
            ResultSet rs = st.executeQuery(sSQL);
            // Llene la tabla con los datos de registro y pasar estos datos a un elemento jTable1 del formulario 
            while (rs.next())
            {
                registro[0]=rs.getString("idalumno");
                registro[1]=rs.getString("nombre1");
                registro[2]=rs.getString("nombre2");
                registro[3]=rs.getString("apellido1");
                registro[4]=rs.getString("apellido2");
                registro[5]=rs.getString("fecha");
                registro[6]=rs.getString("genero");
                modelo.addRow(registro);
            }
            jTableModificar.setModel(modelo);
            
            
            // En caso de error puede llenar el mensaje de error
            } catch (SQLException ex) {
              JOptionPane.showMessageDialog(null, "Mensaje de Error de lectura de datos"); 
            }
        }
    }//GEN-LAST:event_jButtonDatosAlumnoActionPerformed

